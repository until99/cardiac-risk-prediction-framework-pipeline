WITH
  VITALS_CTE AS (
    SELECT
      NR_ATENDIMENTO,
      MAX(QT_PA_MAX) AS TRESTBPS_PROXY,
      MAX(QT_FC) AS THALACH_PROXY,
      MAX(QT_GLICEMIA_CAPILAR) AS MAX_GLICEMIA,
      MIN(QT_SAT) AS QT_SAT_MIN,
      MAX(QT_IMC) AS QT_IMC_MAX,
      MAX(QT_TEMPERATURA) AS QT_TEMP_MAX
    FROM
      SINAL_VITAL_V
    GROUP BY
      NR_ATENDIMENTO
  )
SELECT
  A.NR_ATENDIMENTO,
  A.CD_PESSOA_FISICA,
  A.DT_ENTRADA AS DT_ATENDIMENTO,
  TRUNC (
    MONTHS_BETWEEN (A.DT_ENTRADA, PF.DT_NASCIMENTO) / 12
  ) AS AGE,
  CASE
    WHEN PF.IE_SEXO = 'M' THEN 1
    ELSE 0
  END AS SEX,
  CAST(NULL AS NUMBER) AS CP,
  -- PRESSÃO ARTERIAL SISTÓLICA
  V.TRESTBPS_PROXY AS TRESTBPS,
  CAST(NULL AS NUMBER) AS CHOL,
  -- GLICEMIA CAPILAR COMO PROXY PARA FBS > 120
  CASE
    WHEN V.MAX_GLICEMIA > 120 THEN 1
    WHEN V.MAX_GLICEMIA IS NOT NULL THEN 0
    ELSE NULL
  END AS FBS,
  CAST(NULL AS NUMBER) AS RESTECG,
  -- MAPEADO: FREQUÊNCIA CARDÍACA (MÁXIMA ATINGIDA NO PLANTÃO)
  V.THALACH_PROXY AS THALACH,
  CAST(NULL AS NUMBER) AS EXANG,
  CAST(NULL AS NUMBER) AS OLDPEAK,
  CAST(NULL AS NUMBER) AS SLOPE,
  CAST(NULL AS NUMBER) AS CA,
  CAST(NULL AS NUMBER) AS THAL,
  -- COLUNAS EXTRAS DE INTERESSE CLÍNICO
  V.QT_SAT_MIN,
  V.QT_IMC_MAX,
  V.QT_TEMP_MAX
FROM
  ATENDIMENTO_PACIENTE A
  INNER JOIN PESSOA_FISICA PF ON A.CD_PESSOA_FISICA = PF.CD_PESSOA_FISICA
  LEFT JOIN VITALS_CTE V ON A.NR_ATENDIMENTO = V.NR_ATENDIMENTO
WHERE
  a.dt_entrada >= :dt_inicio 
  AND a.dt_entrada < :dt_fim