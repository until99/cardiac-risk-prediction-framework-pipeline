WITH
	VITALS_AGG AS (
		-- AGREGAÇÃO DOS SINAIS VITAIS POR ATENDIMENTO DENTRO DO PERÍODO
		SELECT
			NR_ATENDIMENTO,
			MAX(QT_PA_MAX) AS TRESTBPS_PROXY,
			MAX(QT_FC) AS THALACH_PROXY,
			MAX(QT_GLICEMIA_CAPILAR) AS MAX_GLICEMIA,
			MIN(QT_SAT) AS QT_SAT_MIN,
			MAX(QT_IMC) AS QT_IMC_MAX,
			MAX(QT_TEMPERATURA) AS QT_TEMP_MAX
		FROM
			SINAL_VITAL_V
		GROUP BY
			NR_ATENDIMENTO
	),
	CARDIAC_DIAGNOSES AS (
		-- IDENTIFICA ATENDIMENTOS QUE TIVERAM DESFECHO/DIAGNÓSTICO CONFIRMADO DE RISCO CARDÍACO (CID-10: I20 A I25)
		SELECT DISTINCT
			NR_ATENDIMENTO
		FROM
			DIAGNOSTICO_DOENCA DD
		WHERE
			-- FILTRA CIDS DE ANGINA, INFARTO AGUDO DO MIOCÁRDIO E DOENÇAS ISQUÊMICAS CRÔNICAS DO CORAÇÃO
			-- A COLUNA CD_DOENCA CONTÉM O CÓDIGO DO CID
			SUBSTR (DD.CD_DOENCA, 1, 3) IN ('I20', 'I21', 'I22', 'I23', 'I24', 'I25')
			-- STATUS 'A' (ATIVO) OU 'P' (PRINCIPAL), DEPENDENDO DA REGRA DE NEGÓCIO DO HOSPITAL. 
			-- REMOVA O FILTRO DE IE_SITUACAO SE QUISER CONSIDERAR DIAGNÓSTICOS INATIVOS TAMBÉM.
			AND DD.IE_SITUACAO = 'A'
	)
SELECT
	A.NR_ATENDIMENTO,
	A.CD_PESSOA_FISICA,
	A.DT_ENTRADA AS DT_ATENDIMENTO,
	-- 1. AGE: IDADE DO PACIENTE NA DATA DO ATENDIMENTO HISTÓRICO
	TRUNC (
		MONTHS_BETWEEN (A.DT_ENTRADA, PF.DT_NASCIMENTO) / 12
	) AS AGE,
	-- 2. SEX: MAPEAMENTO BINÁRIO DE SEXO
	CASE
		WHEN PF.IE_SEXO = 'M' THEN 1
		ELSE 0
	END AS SEX,
	-- 3. CP: TIPO DE DOR NO PEITO (ESTRUTURAR SE HOUVER CAMPO NA TRIAGEM, CASO CONTRÁRIO INICIA COMO NULL)
	CAST(NULL AS NUMBER) AS CP,
	-- 4. TRESTBPS: PRESSÃO ARTERIAL SISTÓLICA MÁXIMA REGISTRADA
	V.TRESTBPS_PROXY AS TRESTBPS,
	-- 5. CHOL: COLESTEROL SÉRICO (PODE SER MAPEADO VIA RESULTADO DE LABORATÓRIO SE NECESSÁRIO)
	CAST(NULL AS NUMBER) AS CHOL,
	-- 6. FBS: GLICEMIA DE JEJUM / CAPILAR > 120 MG/DL (1 = VERDADEIRO; 0 = FALSO)
	CASE
		WHEN V.MAX_GLICEMIA > 120 THEN 1
		WHEN V.MAX_GLICEMIA IS NOT NULL THEN 0
		ELSE NULL
	END AS FBS,
	-- 7. RESTECG: RESULTADOS ELETROCARDIOGRÁFICOS
	CAST(NULL AS NUMBER) AS RESTECG,
	-- 8. THALACH: FREQUÊNCIA CARDÍACA MÁXIMA ATINGIDA
	V.THALACH_PROXY AS THALACH,
	-- 9. EXANG: ANGINA INDUZIDA POR EXERCÍCIO
	CAST(NULL AS NUMBER) AS EXANG,
	-- 10. OLDPEAK: DEPRESSÃO DO SEGMENTO ST
	CAST(NULL AS NUMBER) AS OLDPEAK,
	-- 11. SLOPE: INCLINAÇÃO DO SEGMENTO ST
	CAST(NULL AS NUMBER) AS SLOPE,
	-- 12. CA: NÚMERO DE VASOS PRINCIPAIS
	CAST(NULL AS NUMBER) AS CA,
	-- 13. THAL: TIPO DE TALASSEMIA
	CAST(NULL AS NUMBER) AS THAL,
	-- FEATURES CLÍNICAS ADICIONAIS PARA ENRIQUECIMENTO DO MODELO NO CONTEXTO HOSPITALAR
	V.QT_SAT_MIN,
	V.QT_IMC_MAX,
	V.QT_TEMP_MAX,
	-- TARGET (VARIÁVEL ALVO): 1 SE O ATENDIMENTO EVOLUIU PARA UM CID CARDÍACO CONFIRMADO, 0 CASO CONTRÁRIO
	CASE
		WHEN CD.NR_ATENDIMENTO IS NOT NULL THEN 1
		ELSE 0
	END AS TARGET
FROM
	ATENDIMENTO_PACIENTE A
	INNER JOIN PESSOA_FISICA PF ON A.CD_PESSOA_FISICA = PF.CD_PESSOA_FISICA
	LEFT JOIN VITALS_AGG V ON A.NR_ATENDIMENTO = V.NR_ATENDIMENTO
	LEFT JOIN CARDIAC_DIAGNOSES CD ON A.NR_ATENDIMENTO = CD.NR_ATENDIMENTO
WHERE
	1 = 1
	-- A.DT_ENTRADA >= :DT_INICIO
	-- AND A.DT_ENTRADA < :DT_FIM
	--	AND A.DT_ENTRADA >= TO_DATE('01/01/2026', 'DD/MM/YYYY')
	--	AND A.DT_ENTRADA < TO_DATE('31/05/2026', 'DD/MM/YYYY')
	--	AND A.IE_TIPO_ATENDIMENTO IN ('U', 'I')
	--	AND CD.NR_ATENDIMENTO IS NOT NULL